package po;

public class Menu {
  private Long menu_id;
  private String menu_name;
  private String menu_url;
  private Long menu_level;
  private String menu_icon;
  private Long menu_order;
  private Long menu_status;

  public Long getMenu_id() {
    return menu_id;
  }

  public void setMenu_id(Long menu_id) {
    this.menu_id = menu_id;
  }

  public String getMenu_name() {
    return menu_name;
  }

  public void setMenu_name(String menu_name) {
    this.menu_name = menu_name;
  }

  public String getMenu_url() {
    return menu_url;
  }

  public void setMenu_url(String menu_url) {
    this.menu_url = menu_url;
  }

  public Long getMenu_level() {
    return menu_level;
  }

  public void setMenu_level(Long menu_level) {
    this.menu_level = menu_level;
  }

  public String getMenu_icon() {
    return menu_icon;
  }

  public void setMenu_icon(String menu_icon) {
    this.menu_icon = menu_icon;
  }

  public Long getMenu_order() {
    return menu_order;
  }

  public void setMenu_order(Long menu_order) {
    this.menu_order = menu_order;
  }

  public Long getMenu_status() {
    return menu_status;
  }

  public void setMenu_status(Long menu_status) {
    this.menu_status = menu_status;
  }
}
